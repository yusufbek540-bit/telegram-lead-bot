/**
 * Vercel serverless function — OpenAI proxy for the CRM Ask AI panel.
 *
 * The OpenAI API key lives in Vercel environment variables (server-side only).
 * The CRM browser calls POST /api/ask instead of OpenAI directly.
 *
 * Set in Vercel dashboard: Settings → Environment Variables → OPENAI_API_KEY
 */
export default async function handler(req, res) {
  if (req.method !== 'POST') {
    return res.status(405).json({ error: 'Method not allowed' });
  }

  const apiKey = process.env.OPENAI_API_KEY;
  if (!apiKey) {
    return res.status(500).json({ error: 'OPENAI_API_KEY not configured on server' });
  }

  try {
    const response = await fetch('https://api.openai.com/v1/chat/completions', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${apiKey}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify(req.body),
    });

    const data = await response.json();
    res.status(response.status).json(data);
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
}
